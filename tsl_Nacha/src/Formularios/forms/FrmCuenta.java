    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
           try{
    String s = String.valueOf(jTable2.getValueAt(jTable2.getSelectedRow(), 0));
        int numero = Integer.parseInt(s);
            
       Cuentas c = GestCuentas.getInstance().devolverCuentaPorNumero(numero);
       if(GestCuentas.getInstance().eliminarCuenta(c)){
           JOptionPane.showMessageDialog(rootPane, "Cuenta Eliminada");
               DefaultTableModel modelo = (DefaultTableModel) this.jTable2.getModel();
                Utilitaria.limpiarJtables(modelo);
                Utilitaria.listarCuentas(modelo);
           
       }
       else{
           JOptionPane.showMessageDialog(rootPane,"La cuenta tiene un Saldo positivo / La cuenta NACHA CONTADO no se puede eliminar");
       }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Compruebe si selecciono un Articulo");
        }
}//GEN-LAST:event_btnEliminarActionPerformed


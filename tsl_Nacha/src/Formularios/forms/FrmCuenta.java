    private void btnAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar1ActionPerformed
        // TODO add your handling code here:
        if(!GestCuentas.getInstance().clienteTieneCuenta( (Clientes)this.jComboBox1.getSelectedItem())){
        Cuentas nuevaCuenta = new Cuentas(GestCuentas.getInstance().devolverNumeroCuenta(), (Clientes)this.jComboBox1.getSelectedItem(), Double.parseDouble(this.txttelefono.getText()),null, true);
        GestCuentas.getInstance().agregarCuenta(nuevaCuenta);
        Utilitaria.limpiarJtables((DefaultTableModel)this.jTable2.getModel());
        Utilitaria.listarCuentas((DefaultTableModel)this.jTable2.getModel());
        }else
        {
            JOptionPane.showMessageDialog(rootPane, "Actualmente se permite una cuenta por cliente");
        }
}//GEN-LAST:event_btnAgregar1ActionPerformed


        private void btnAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar1ActionPerformed
            // TODO add your handling code here:
            try {
                if ("".equals(txtmatricula.getText()) || "".equals(txtmarca.getText()) || "".equals(txtejes.getText()) || "".equals(txtmetros.getText())) {
                    JOptionPane.showMessageDialog(null, "Falto ingresar algun dato obligatorio.");
                } else {
                    DefaultTableModel modelo = (DefaultTableModel) jTableCamiones.getModel();
                    for (int i = 0; i < modelo.getRowCount();) {
                        modelo.removeRow(i);
                    }
                    if("".equals(txtcolor.getText())){
                        txtcolor.setText("No ingreso");
                    }
                    Camion objC = new Camion(txtmatricula.getText(), txtmarca.getText(), txtcolor.getText(), Integer.parseInt(txtejes.getText()), Double.parseDouble(txtmetros.getText()));
                    GestCamiones.getInstance().altaCamiones(objC);
                    //this.txtNumero.setText(String.valueOf(GestEmpleado.getInstance().devolverUltimoNum()));
                    for (Camion objcam : GestCamiones.getInstance().devolverCamiones()) {
                        Object[] objOp = {objcam.getcMatricula(), objcam.getcMarca(), objcam.getcColor(), objcam.getcEjes(), objcam.getcMetros()};
                        modelo.addRow(objOp);
                    }
                }
                this.txtmatricula.setText("");
                this.txtmarca.setText("");
                this.txtcolor.setText("");
                this.txtejes.setText("");
                this.txtmetros.setText("");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Hubo un error interno mientras se agregaba el camion. \nSi el problema persiste comuniquese a W.O.R.A inc");
            }
        }//GEN-LAST:event_btnAgregar1ActionPerformed

        private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
            // TODO add your handling code here:
            if (jTableCamiones.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(null, "Seleccione un camoin");
            } else {
                String x = (String.valueOf(this.jTableCamiones.getValueAt(jTableCamiones.getSelectedRow(), 0)));
                /*if (AppAdministracion.getInstance().buscarSiLocalTieneOperador(numero) == false) {
                JOptionPane.showMessageDialog(null, "ALGUN LOCAL ESTA ASOCIADO AL OPERADOR,\n IMPOSIBLE DE ELIMINAR", "Fatal Error", JOptionPane.ERROR_MESSAGE);
                } else {*/
                if ((GestCamiones.getInstance().bajaCamiones(x)) == true) {
                    DefaultTableModel modelo = (DefaultTableModel) jTableCamiones.getModel();
                    for (int i = 0; i < modelo.getRowCount();) {
                        modelo.removeRow(i);
                    }
                    Utilitaria.listarCamiones((DefaultTableModel) jTableCamiones.getModel());
                    txtmatricula.setText("");
                    txtmarca.setText("");
                    txtcolor.setText("");
                    txtejes.setText("");
                    txtmetros.setText("");
                } else {
                    JOptionPane.showMessageDialog(jTableCamiones, "No se puede eliminar el camion. \n Esta en algun registro de facturas o gastos");
                }


            }
        }//GEN-LAST:event_btnEliminarActionPerformed


        private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
            // TODO add your handling code here:
            if ("".equals(txtmatricula.getText()) || "".equals(txtmarca.getText()) || "".equals(txtejes.getText()) || "".equals(txtmetros.getText()) || "".equals(txtcolor.getText())) {
                JOptionPane.showMessageDialog(null, "Error al modificar el Camion. \n Falto ingresar algun dato.");
            } else {
                Camion objCamion = new Camion(txtmatricula.getText(), txtmarca.getText(), txtcolor.getText(), Integer.parseInt(txtejes.getText()), Double.parseDouble(txtmetros.getText()));
                GestCamiones.getInstance().modificarCamiones(objCamion);
                txtmatricula.setText("");
                txtmarca.setText("");
                txtcolor.setText("");
                txtejes.setText("");
                txtmetros.setText("");
                Utilitaria.listarCamiones((DefaultTableModel) jTableCamiones.getModel());
            }
        }//GEN-LAST:event_btnAgregarActionPerformed


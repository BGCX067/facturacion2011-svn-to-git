    private void btnimprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnimprimirActionPerformed
        Date date = new Date();
        Date date2 = new Date();
        date = (jDateChooser1.getDate());
        date2 = (jDateChooser2.getDate());
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String fechadesde = dateFormat.format(date);
        String fechahasta = dateFormat.format(date2);
        MessageFormat encabezado1 = new MessageFormat("Gastos del Camion  " + jComboBox1.getSelectedItem().toString());
        MessageFormat encabezado2 = new MessageFormat("Desde: " + fechadesde + "   Hasta: " + fechahasta);
                if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(rootPane, "No se puede imprimir. \n Esta en blanco la lista");
        } else {
            try {
                // TODO add your handling code here:
                jTable1.print(PrintMode.FIT_WIDTH, encabezado1,encabezado2);
            } catch (PrinterException ex) {
                Logger.getLogger(FrmConsultaEmpleados.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnimprimirActionPerformed

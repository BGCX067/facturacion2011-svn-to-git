private void btnAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar1ActionPerformed
// TODO add your handling code here:
    try {
        if ("".equals(txtNombre1.getText())) {
            JOptionPane.showMessageDialog(null, "Falto ingresar algun dato. \n Recuerde que los datos obligatorios son los datos de la columna de la izquierda");
        } else {
            Date date = new Date();
            date = (txtfingreso.getDate());
            DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            String fecha = dateFormat.format(date);
            DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
            for (int i = 0; i < modelo.getRowCount();) {
                modelo.removeRow(i);
            }
            if ("".equals(txtTelefono.getText())) {
                txtTelefono.setText("No ingreso");
            } else if ("".equals(txtDireccion.getText())) {
                txtDireccion.setText("No ingreso");
            } else if ("".equals(txtCedula.getText())) {
                txtCedula.setText("No ingreso");
            }
            Empleado objEmp = new Empleado(txtCedula.getText(), fecha, cboxCargo.getSelectedItem().toString(), Integer.parseInt(txtNumero.getText()), txtNombre1.getText(), txtDireccion.getText(), txtTelefono.getText());
            GestEmpleado.getInstance().altaEmpleado(objEmp);
            this.txtNumero.setText(String.valueOf(GestEmpleado.getInstance().devolverUltimoNum()));
            for (Empleado obje : GestEmpleado.getInstance().devolverEmpleado()) {
                Object[] objOp = {obje.getpNumero(), obje.getpNombre(), obje.getpDireccion(), obje.getpTelefono(), obje.geteCedulaIdentidad(), obje.geteFechaIngreso(), obje.geteCargo()};
                modelo.addRow(objOp);
            }
        }
        this.txtNombre1.setText("");
        this.txtCedula.setText("");
        this.txtDireccion.setText("");
        this.txtTelefono.setText("");
    } catch (NullPointerException a) {
        JOptionPane.showMessageDialog(rootPane, "Selecione la fecha de ingreso");
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Hubo un error interno mientras se agregaba el empleado. \nSi el problema persiste comuniquese a W.O.R.A inc");
    }
}//GEN-LAST:event_btnAgregar1ActionPerformed


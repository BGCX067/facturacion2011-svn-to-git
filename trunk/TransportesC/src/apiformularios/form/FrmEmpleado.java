private void txtNombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyReleased
    // TODO add your handling code here:
    try {
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        modelo.setRowCount(0);
        String nombre = txtNombre.getText();
        int i = nombre.length();
        String nombre2;
        for (Empleado obje : GestEmpleado.getInstance().devolverEmpleado()) {
            nombre2 = obje.getpNombre();
            String cortado = nombre2.substring(0, i);
            if (cortado.equalsIgnoreCase(nombre)) {
                Object[] objcli = {obje.getpNumero(), obje.getpNombre(), obje.getpDireccion(), obje.getpTelefono(), obje.geteCedulaIdentidad(), obje.geteFechaIngreso(), obje.geteCargo()};
                modelo.addRow(objcli);
            }
        }
    } catch (Exception e) {
    }
}//GEN-LAST:event_txtNombreKeyReleased


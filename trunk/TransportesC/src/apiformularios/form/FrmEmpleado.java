    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (jTable2.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un empleado");
        } else {
            String x = (String.valueOf(this.jTable2.getValueAt(jTable2.getSelectedRow(), 0)));
            int numero = Integer.parseInt(x);
            /*if (AppAdministracion.getInstance().buscarSiLocalTieneOperador(numero) == false) {
            JOptionPane.showMessageDialog(null, "ALGUN LOCAL ESTA ASOCIADO AL OPERADOR,\n IMPOSIBLE DE ELIMINAR", "Fatal Error", JOptionPane.ERROR_MESSAGE);
            } else {*/
            if (GestEmpleado.getInstance().bajaEmpleado(numero) == true) {
                JOptionPane.showMessageDialog(rootPane, "Empleado eliminado correctamente");
                this.btnAgregar1.setEnabled(true);
                this.btnAgregar.setEnabled(false);
                this.txtNumero.setText(String.valueOf(GestEmpleado.getInstance().devolverUltimoNum()));
                this.txtNombre1.setText("");
                this.txtCedula.setText("");
                this.txtDireccion.setText("");
                this.txtTelefono.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se puede eliminar el empleado. \n El registro esta en alguna factura");
            
            }
            DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
            for (int i = 0; i < modelo.getRowCount();) {
                modelo.removeRow(i);
            }
            for (Empleado obje : GestEmpleado.getInstance().devolverEmpleado()) {
                Object[] objOp = {obje.getpNumero(), obje.getpNombre(), obje.getpDireccion(), obje.getpTelefono(), obje.geteCedulaIdentidad(), obje.geteFechaIngreso(), obje.geteCargo()};
                modelo.addRow(objOp);
            }
        }

    }//GEN-LAST:event_btnEliminarActionPerformed


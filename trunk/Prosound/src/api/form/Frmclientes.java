    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if ("".equals(lblnumero.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Falto ingresar codigo");
        }
        if ("".equals(txtnombre.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Falto ingresar nombre");
        }
        if ("".equals(txtrut.getText())) {
            txtrut.setText("0");
        }
        if ("".equals(txtdireccion.getText())) {
            txtdireccion.setText("0");
        }
        if ("".equals(txttelefono.getText())) {
            txttelefono.setText("0");
        } else {
            //convierto los datos
            int codigo = Integer.parseInt(lblnumero.getText());
            String nombre = txtnombre.getText();
            String fing = Utilitaria.convertirDateString(jDateChooser1.getDate());
            String rut = txtrut.getText();
            String tel = txttelefono.getText();
            String direccion = txtdireccion.getText();
            //creo un articulo temporal
            Cliente objC = new Cliente(codigo, nombre, fing, rut, tel, direccion);
            //lo modifico
            GestCliente.getInstance().modificarCliente(objC);
            //listo los clientes
            Utilitaria.listarClientes((DefaultTableModel) jTable1.getModel());
            lblnumero.setText(String.valueOf(GestCliente.getInstance().devolverUltimoNum()));
            txtnombre.setText("");
            txtdireccion.setText("");
            txtrut.setText("");
            txttelefono.setText("");
            jDateChooser1.setDate(null);
            jButton3.setEnabled(false);
            jButton2.setEnabled(true);
            JOptionPane.showMessageDialog(rootPane, "Cliente modificado");
        }
    }//GEN-LAST:event_jButton3ActionPerformed


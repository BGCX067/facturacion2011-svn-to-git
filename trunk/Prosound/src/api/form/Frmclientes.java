    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if ("".equals(lblnumero.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Falto ingresar codigo");
        }
        else if ("".equals(txtnombre.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Falto ingresar nombre");
        }
        else if ("".equals(txtrut.getText())) {
            txtrut.setText("0");
        }
        else if ("".equals(txtdireccion.getText())) {
            txtdireccion.setText("0");
        }
        else if ("".equals(txttelefono.getText())) {
            txttelefono.setText("0");
        } else {
            //convierto los datos
            int codigo = Integer.parseInt(lblnumero.getText());
            String nombre = txtnombre.getText();
            String fing = Utilitaria.convertirDateString(jDateChooser1.getDate());
            String rut = txtrut.getText();
            String tel = txttelefono.getText();
            String direccion = txtdireccion.getText();
            //creo el cliente
            Cliente objC = new Cliente(codigo, nombre, fing, rut, tel, direccion);
            //lo agrego
            GestCliente.getInstance().altaCliente(objC);
            //mensaje
            JOptionPane.showMessageDialog(rootPane, "Cliente0 agregado exitosamente");
            //listo los articulos y cambio el nuevo numero
            Utilitaria.listarClientes((DefaultTableModel) jTable1.getModel());
            lblnumero.setText(String.valueOf(GestCliente.getInstance().devolverUltimoNum()));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void txtNombre1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombre1KeyReleased
        // TODO add your handling code here:
        try {
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);
            String nombre = txtNombre1.getText();
            int i = nombre.length();
            String nombre2;
            for (Cliente objC : GestCliente.getInstance().devolverCliente()) {
                nombre2 = objC.getCliNom();
                String cortado = nombre2.substring(0, i);
                if (cortado.equalsIgnoreCase(nombre)) {
                    Object[] objart = {objC.getCliCod(), objC.getCliNom(), objC.getCliFing(), objC.getCliRut(), objC.getCliTel(), objC.getCliDire()};
                    modelo.addRow(objart);
                }
            }
        } catch (Exception e) {
        }
}//GEN-LAST:event_txtNombre1KeyReleased

